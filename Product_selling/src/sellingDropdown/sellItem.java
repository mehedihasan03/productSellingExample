    private void comboItemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboItemItemStateChanged

        int index = comboItem.getSelectedIndex();

        if (index != -1) {
            Product p = pList.get(index);
            tfAvailableQty.setText(String.valueOf(p.getQty()));
            if (p.getQty() > 0) {
                tfSellQty.setEnabled(true);
                jButton1.setEnabled(true);
            } else {
                tfSellQty.setEnabled(false);
                jButton1.setEnabled(false);
            }
        } 
    }//GEN-LAST:event_comboItemItemStateChanged

