    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Connection conn = DataConnection.connect();
            String sql = "update newproducts set qty=? where id = ?;";
            PreparedStatement prst = conn.prepareStatement(sql);
            int sellQty = Integer.parseInt(tfSellQty.getText());
            int availableQty = Integer.parseInt(tfAvailableQty.getText());
            int remainQty = availableQty - sellQty;
            prst.setInt(1, remainQty);
            int index = comboItem.getSelectedIndex();
            Product p = pList.get(index);
            prst.setInt(2, p.getId());
            prst.executeUpdate();
            comboItemLoader();
            tfSellQty.setText(null);

        } catch (SQLException ex) {
            Logger.getLogger(sellItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

